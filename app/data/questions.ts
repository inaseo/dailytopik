function normalizeQuestion(raw: any, index: number): Question | null {
    if (!raw || typeof raw !== 'object') return null;

    // 1. ID Normalization
    const id = typeof raw.id === 'number' ? raw.id : index + 1;

    // 2. Type Normalization
    let type: QuestionType = "passage"; // default fallback
    const rawType = String(raw.type || "").toLowerCase();

    // Check if it's already a supported type
    const validTypes: QuestionType[] = ["blank", "passage", "ordering", "underline", "connector", "title", "sentence", "mainIdea", "purpose"];
    if (validTypes.includes(rawType as QuestionType)) {
        type = rawType as QuestionType;
    } else {
        // Fallback or mapping if needed (mostly unnecessary with clean data)
        const mapping: { [key: string]: QuestionType } = {
            "vocab_grammar": "blank",
            "short_reading": "passage",
            "long_reading": "passage"
        };
        if (mapping[rawType]) type = mapping[rawType];
    }

    // 3. Choices Normalization (Map options/choices -> choices)
    let choices: string[] | null = null;
    let rawOptionsCandidate = raw.options || raw.choices || raw.items;

    if (Array.isArray(rawOptionsCandidate)) {
        choices = rawOptionsCandidate;
    } else if (rawOptionsCandidate && typeof rawOptionsCandidate === 'object') {
        const sortedKeys = Object.keys(rawOptionsCandidate).sort();
        const values = sortedKeys.map(k => rawOptionsCandidate[k]);
        if (values.length === 4) choices = values;
    } else if (typeof rawOptionsCandidate === 'string') {
        const lines = rawOptionsCandidate.split('\n').map(line => line.trim()).filter(Boolean);
        const parsed = lines.map(line => line.replace(/^[0-9]+\.\s*/, ''));
        if (parsed.length === 4) choices = parsed;
    }

    if (!choices || choices.length !== 4) return null;
    choices = choices.map(opt => String(opt));

    // 4. Answer Normalization (Map answerIndex -> answer)
    let answer = -1;
    if (typeof raw.answerIndex === 'number' && raw.answerIndex >= 0 && raw.answerIndex <= 3) {
        answer = raw.answerIndex;
    } else if (typeof raw.answer === 'number' && raw.answer >= 0 && raw.answer <= 3) { // Allow explicit new field
        answer = raw.answer;
    } else if (typeof raw.correct_answer === 'number') {
        answer = raw.correct_answer;
    }

    if (answer < 0 || answer > 3) return null;

    // 5. Text Fields Normalization
    // instruction: keep or fallback
    let instruction = typeof raw.instruction === 'string' ? raw.instruction : "다음을 읽고 물음에 답하십시오.";

    // passage: optional context/reading
    let passage = typeof raw.passage === 'string' ? raw.passage : undefined;

    // question: main prompt
    let question = typeof raw.question === 'string' ? raw.question : "";

    // Legacy fallback: if question is empty but question_text exists?
    if (!question && raw.question_text) question = raw.question_text;

    // Special handling for passage/ordering/title to fix duplicate text
    if (["passage", "ordering", "title"].includes(type)) {
        // If question and instruction are identical (or instruction is the specific prompt),
        // Move specific prompt to question (if not already there) and make instruction generic.

        // In provided data, raw.instruction AND raw.question are both specific (e.g., "Choose matching content").
        // We want: instruction="Read...", question="Choose matching..."

        // Ensure question has the specific prompt (priority to original instruction if question is missing/same)
        if (!question && instruction) question = instruction;

        // Overwrite instruction with generic caption
        instruction = "다음을 읽고 물음에 답하십시오.";
    }

    // Fallback: if passage missing and type suggests checks?
    // Actually, type "passage", "ordering", "title" usually need passage.
    // If passage is missing but content was unified before?
    // We assume clean data now.

    if (!question.trim()) {
        // Worst case fallback: map content to question if strictly one string?
        // But we want separation.
        if (raw.content) question = raw.content;
        else return null;
    }

    return {
        id,
        type,
        instruction,
        passage,
        question,
        choices,
        answer
    };
}

export const questions: Question[] = rawQuestions
    .map((raw, idx) => normalizeQuestion(raw, idx))
    .filter((q): q is Question => q !== null);
